# ── Load and scale fingerprint dataset ───────────────────────────────────────

def load_and_scale_fingerprints(SCENE_DIR):
    """Load the fingerprint HDF5 cache and apply standard scaling.

    Parameters
    ----------
    SCENE_DIR : Path
        Scene directory containing ``fingerprint_rt_dataset.h5``.

    Returns
    -------
    X_cc_scaled : (N, D) ndarray
        Standardised feature matrix (zero-mean, unit-variance per feature).
    pos_cc : (N, 2) ndarray
        Corresponding [x, y] UE positions.
    N : int
        Total number of samples.
    """
    from sklearn.preprocessing import StandardScaler
    from features import load_fingerprint_dataset

    csi_fingerprints, fingerprint_positions = load_fingerprint_dataset(SCENE_DIR)
    print(f"Fingerprints: {csi_fingerprints.shape}")

    X_cc   = csi_fingerprints.copy()
    pos_cc = fingerprint_positions.copy()

    scaler_cc   = StandardScaler()
    X_cc_scaled = scaler_cc.fit_transform(X_cc)
    N = X_cc_scaled.shape[0]
    print(f"Dataset ready: N={N} samples, {X_cc_scaled.shape[1]} features")
    return X_cc_scaled, pos_cc, N


# ── Dimensionality reduction methods ─────────────────────────────────────────

def run_pca_charting(X_cc_scaled):
    """Compute a PCA channel chart and print quality metrics.

    Parameters
    ----------
    X_cc_scaled : (N, D) ndarray
        Standardised fingerprint feature matrix.

    Returns
    -------
    pca_res : dict
        Result dict with keys: ``Z`` (2-D embedding), ``tw``, ``ct``, ``ks``,
        ``explained_var``, ``X_pca50`` (50-component PCA used as input to
        t-SNE and UMAP for speed).
    Z_pca : (N, 2) ndarray
        The 2-D PCA embedding.
    """
    from channel_charting import run_pca

    pca_res = run_pca(X_cc_scaled)
    Z_pca   = pca_res['Z']
    print(f"PCA: TW={pca_res['tw']:.4f}  CT={pca_res['ct']:.4f}  KS={pca_res['ks']:.4f}")
    return pca_res, Z_pca


def run_tsne_charting(X_cc_scaled, X_pca50, pos_cc):
    """Compute a t-SNE channel chart, sweeping perplexity, and print metrics.

    Parameters
    ----------
    X_cc_scaled : (N, D) ndarray
        Standardised feature matrix.
    X_pca50 : (N, 50) ndarray
        50-component PCA pre-projection (used as t-SNE input for speed).
    pos_cc : (N, 2) ndarray
        True [x, y] positions (used to compute NNLE).

    Returns
    -------
    tsne_res : dict
        Result dict with keys: ``Z``, ``tw``, ``ct``, ``ks``, ``nnle``,
        ``best_perp``.
    Z_tsne : (N, 2) ndarray
        The best t-SNE 2-D embedding.
    """
    from channel_charting import run_tsne

    tsne_res = run_tsne(X_cc_scaled, X_pca50=X_pca50, pos_true=pos_cc)
    Z_tsne   = tsne_res['Z']
    nnle_str = f"  NNLE={tsne_res['nnle']:.2f} m" if not np.isnan(tsne_res['nnle']) else ""
    print(f"t-SNE (best perp={tsne_res['best_perp']}): "
          f"TW={tsne_res['tw']:.4f}  CT={tsne_res['ct']:.4f}  KS={tsne_res['ks']:.4f}{nnle_str}")
    return tsne_res, Z_tsne


def run_autoencoder_charting(X_cc_scaled, pos_cc):
    """Train an unsupervised autoencoder and compute channel chart metrics.

    Parameters
    ----------
    X_cc_scaled : (N, D) ndarray
        Standardised feature matrix.
    pos_cc : (N, 2) ndarray
        True [x, y] positions (used to compute NNLE).

    Returns
    -------
    ae_res : dict
        Result dict with keys: ``Z``, ``tw``, ``ct``, ``ks``, ``nnle``.
    Z_ae : (N, 2) ndarray
        The 2-D autoencoder latent-space embedding.
    """
    from channel_charting import run_autoencoder

    ae_res = run_autoencoder(X_cc_scaled, pos_true=pos_cc)
    Z_ae   = ae_res['Z']
    nnle_str = f"  NNLE={ae_res['nnle']:.2f} m" if not np.isnan(ae_res['nnle']) else ""
    print(f"AE: TW={ae_res['tw']:.4f}  CT={ae_res['ct']:.4f}  KS={ae_res['ks']:.4f}{nnle_str}")
    return ae_res, Z_ae


def run_umap_charting(X_cc_scaled, X_pca50, pos_cc):
    """Compute a UMAP channel chart if ``umap-learn`` is installed.

    Parameters
    ----------
    X_cc_scaled : (N, D) ndarray
        Standardised feature matrix.
    X_pca50 : (N, 50) ndarray
        50-component PCA pre-projection (used as UMAP input for speed).
    pos_cc : (N, 2) ndarray
        True [x, y] positions (used to compute NNLE).

    Returns
    -------
    umap_res : dict
        Result dict with keys: ``Z``, ``tw``, ``ct``, ``ks``, ``nnle``,
        ``best_n``.  Empty dict if ``umap-learn`` is not installed.
    Z_umap : (N, 2) ndarray or None
        The 2-D UMAP embedding, or ``None`` if UMAP is unavailable.
    UMAP_OK : bool
        ``True`` when UMAP ran successfully.
    """
    try:
        from channel_charting import run_umap

        umap_res = run_umap(X_cc_scaled, X_pca50=X_pca50, pos_true=pos_cc)
        Z_umap   = umap_res['Z']
        UMAP_OK  = True
        nnle_str = f"  NNLE={umap_res['nnle']:.2f} m" if not np.isnan(umap_res['nnle']) else ""
        print(f"UMAP (best n={umap_res['best_n']}): "
              f"TW={umap_res['tw']:.4f}  CT={umap_res['ct']:.4f}  KS={umap_res['ks']:.4f}{nnle_str}")
    except ImportError:
        print("umap-learn not installed — install with: pip install umap-learn")
        umap_res = {}
        Z_umap   = None
        UMAP_OK  = False
    return umap_res, Z_umap, UMAP_OK


# ── Visualise channel charts ──────────────────────────────────────────────────

def visualise_channel_charts(embeddings, titles,
                              pos_cc, cfg, PICTURES_DIR):
    """Save channel chart figures coloured by physical X, Y, and distance.

    Produces three figures:
    * ``channel_charts_coloured_x.png`` — coloured by X coordinate
    * ``channel_charts_coloured_y.png`` — coloured by Y coordinate
    * ``channel_charts_comparison.png`` — coloured by distance from scene centre

    Smooth, monotone gradients confirm the chart preserves spatial order.

    Parameters
    ----------
    embeddings : list of (N, 2) ndarray
        2-D embeddings for each enabled method.
    titles : list of str
        Per-method subplot titles (include metric values).
    pos_cc : (N, 2) ndarray
        True [x, y] UE positions.
    cfg : dict
        Scene configuration — requires ``SCENE_CX`` and ``SCENE_CY``.
    PICTURES_DIR : Path
        Output directory for the figures.
    """
    n_charts = len(embeddings)

    for axis_idx, (axis_label, cmap, fname) in enumerate([
        ('X (m)', 'viridis',  'channel_charts_coloured_x.png'),
        ('Y (m)', 'plasma',   'channel_charts_coloured_y.png'),
    ]):
        color_vals = pos_cc[:, axis_idx]
        fig, axes = plt.subplots(1, n_charts, figsize=(6 * n_charts, 5))
        if n_charts == 1:
            axes = [axes]
        for ax, Z, title in zip(axes, embeddings, titles):
            sc = ax.scatter(Z[:, 0], Z[:, 1], c=color_vals, cmap=cmap,
                            s=18, alpha=0.8)
            plt.colorbar(sc, ax=ax, label=axis_label)
            ax.set_title(title, fontsize=9, fontweight='bold')
            ax.set_xlabel('Dim 1'); ax.set_ylabel('Dim 2')
            ax.grid(True, alpha=0.2)
        plt.suptitle(f'Channel Charts — coloured by physical {axis_label}',
                     fontsize=12, fontweight='bold', y=1.01)
        plt.tight_layout()
        plt.savefig(PICTURES_DIR / fname, dpi=150, bbox_inches='tight')
        plt.close()

    # Distance-from-centre figure (summary / report overview)
    scene_center  = np.array([cfg['SCENE_CX'], cfg['SCENE_CY']])
    color_by_dist = np.linalg.norm(pos_cc - scene_center, axis=1)
    fig, axes = plt.subplots(1, n_charts, figsize=(6 * n_charts, 5))
    if n_charts == 1:
        axes = [axes]
    for ax, Z, title in zip(axes, embeddings, titles):
        sc = ax.scatter(Z[:, 0], Z[:, 1], c=color_by_dist, cmap='plasma',
                        s=18, alpha=0.8)
        plt.colorbar(sc, ax=ax, label='Dist from centre (m)')
        ax.set_title(title, fontsize=9, fontweight='bold')
        ax.set_xlabel('Dim 1'); ax.set_ylabel('Dim 2')
        ax.grid(True, alpha=0.2)
    plt.suptitle('Channel Charts — coloured by distance from scene centre',
                 fontsize=12, fontweight='bold', y=1.01)
    plt.tight_layout()
    plt.savefig(PICTURES_DIR / 'channel_charts_comparison.png', dpi=150,
                bbox_inches='tight')
    plt.close()


# ── PCA explained variance ────────────────────────────────────────────────────

def plot_pca_variance(pca_res, PICTURES_DIR):
    """Save a cumulative explained-variance plot for PCA.

    Horizontal reference lines are drawn at 90 % and 95 % to indicate how
    many components are needed to explain most of the variance.

    Parameters
    ----------
    pca_res : dict
        PCA result dict containing ``explained_var`` (cumulative variance
        array from :func:`channel_charting.run_pca`).
    PICTURES_DIR : Path
        Output directory for ``pca_explained_variance.png``.
    """
    fig, ax = plt.subplots(figsize=(8, 4))
    ax.plot(np.arange(1, len(pca_res['explained_var']) + 1),
            pca_res['explained_var'] * 100, 'o-', lw=2)
    ax.axhline(90, color='red',    ls='--', label='90%')
    ax.axhline(95, color='orange', ls='--', label='95%')
    ax.set_xlabel('Number of Components'); ax.set_ylabel('Cumulative Variance (%)')
    ax.set_title('PCA Explained Variance'); ax.legend(); ax.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig(PICTURES_DIR / 'pca_explained_variance.png', dpi=150)
    # plt.show()


# ── Summary table ─────────────────────────────────────────────────────────────

def build_summary_table(cc_results_list, SCENE_DIR):
    """Print a metrics table and save it to CSV.

    Parameters
    ----------
    cc_results_list : list of (str, dict)
        Sequence of ``(method_name, result_dict)`` pairs for each enabled
        charting method.  Result dicts must contain ``tw``, ``ct``, ``ks``,
        and optionally ``nnle`` and ``best_perp`` / ``best_n``.
    SCENE_DIR : Path
        Directory where ``channel_charting_metrics.csv`` is saved.

    Returns
    -------
    cc_methods : list of str
    tw_vals, ct_vals, ks_vals, nnle_vals : list of float
    """
    cc_methods = [r[0] for r in cc_results_list]
    tw_vals    = [r[1]['tw']  for r in cc_results_list]
    ct_vals    = [r[1]['ct']  for r in cc_results_list]
    ks_vals    = [r[1]['ks']  for r in cc_results_list]
    nnle_vals  = [r[1].get('nnle', float('nan')) for r in cc_results_list]

    df_cc = pd.DataFrame({
        'Method':               cc_methods,
        'TW (↑ better)':       [f'{v:.4f}' for v in tw_vals],
        'CT (↑ better)':       [f'{v:.4f}' for v in ct_vals],
        'KS (↓ better)':       [f'{v:.4f}' for v in ks_vals],
        'NNLE m (↓ better)':   [f'{v:.2f}' if not np.isnan(v) else 'n/a' for v in nnle_vals],
    })
    print("\n" + "=" * 70)
    print("CHANNEL CHARTING SUMMARY")
    print("=" * 70)
    print(df_cc.to_string(index=False))

    csv_out = SCENE_DIR / 'channel_charting_metrics.csv'
    df_cc.to_csv(csv_out, index=False)
    print(f"\nSaved → {csv_out}")
    return cc_methods, tw_vals, ct_vals, ks_vals, nnle_vals


# ── Automated metric interpretation ──────────────────────────────────────────

def print_metric_winners(cc_methods, tw_vals, ct_vals, ks_vals, nnle_vals=None):
    """Print per-metric winner and a qualitative verdict for every method.

    Parameters
    ----------
    cc_methods : list of str
        Method names in the same order as the value lists.
    tw_vals, ct_vals, ks_vals : list of float
        Trustworthiness, Continuity, and Kruskal Stress values.
    nnle_vals : list of float or None
        Nearest-Neighbour Localisation Errors (NaN for methods without NNLE).
    """
    tw_arr = np.array(tw_vals, dtype=float)
    ct_arr = np.array(ct_vals, dtype=float)
    ks_arr = np.array(ks_vals, dtype=float)

    best_tw  = cc_methods[int(np.argmax(tw_arr))]
    best_ct  = cc_methods[int(np.argmax(ct_arr))]
    best_ks  = cc_methods[int(np.argmin(ks_arr))]

    def _norm01(v):
        return (v - v.min()) / ((v.max() - v.min()) + 1e-12)

    combined     = (_norm01(tw_arr) + _norm01(ct_arr) + (1 - _norm01(ks_arr))) / 3
    best_overall = cc_methods[int(np.argmax(combined))]

    nnle_arr = np.array(nnle_vals if nnle_vals is not None else [float('nan')] * len(cc_methods))
    valid_nnle = ~np.isnan(nnle_arr)
    best_nnle = cc_methods[int(np.nanargmin(nnle_arr))] if valid_nnle.any() else 'n/a'

    print("─" * 60)
    print("METRIC WINNERS")
    print("─" * 60)
    print(f"  Best Trustworthiness (TW ↑)  : {best_tw}")
    print(f"  Best Continuity       (CT ↑)  : {best_ct}")
    print(f"  Best KS distance      (KS ↓)  : {best_ks}")
    print(f"  Best NNLE             (↓ m)   : {best_nnle}")
    print(f"  Best overall avg rank         : {best_overall}")
    print("─" * 60)
    print(f"\nTW range : {tw_arr.min():.4f} – {tw_arr.max():.4f}  (1.0 = ideal)")
    print(f"CT range : {ct_arr.min():.4f} – {ct_arr.max():.4f}  (1.0 = ideal)")
    print(f"KS range : {ks_arr.min():.4f} – {ks_arr.max():.4f}  (0.0 = ideal)")
    if valid_nnle.any():
        print(f"NNLE range : {np.nanmin(nnle_arr):.2f} – {np.nanmax(nnle_arr):.2f} m  (0 = ideal)")

    for i, (name, tw, ct, ks, _) in enumerate(zip(cc_methods, tw_arr, ct_arr, ks_arr, combined)):
        flag = " ← best overall" if name == best_overall else ""
        print(f"\n  {name}{flag}")
        verdict_tw = "good" if tw > 0.9 else ("ok" if tw > 0.8 else "poor")
        verdict_ct = "good" if ct > 0.9 else ("ok" if ct > 0.8 else "poor")
        verdict_ks = "good" if ks < 0.1 else ("ok" if ks < 0.2 else "poor")
        nnle_str = ""
        if nnle_vals is not None and not np.isnan(nnle_arr[i]):
            nnle_str = f"  NNLE={nnle_arr[i]:.2f} m"
        print(f"    TW={tw:.4f} ({verdict_tw})  CT={ct:.4f} ({verdict_ct})  KS={ks:.4f} ({verdict_ks}){nnle_str}")


