"""
04_channel_charting.py — Unsupervised channel chart generation.

Applies dimensionality reduction to the CSI fingerprint matrix to produce a
2-D channel chart *without* using position labels.  Which methods are run is
controlled by the scene's ``features_config.json`` (section
``channel_charting``):

  • pca         — Principal Component Analysis (linear, fast baseline)
  • tsne        — t-distributed Stochastic Neighbour Embedding
  • autoencoder — Unsupervised autoencoder with a 2-D latent space
  • umap        — Uniform Manifold Approximation and Projection

Quality metrics: Trustworthiness (TW), Continuity (CT), Kruskal Stress (KS),
and Nearest-Neighbour Localisation Error (NNLE).

Requires: <SCENE_DIR>/fingerprint_rt_dataset.h5
(generated by 01_generate_dataset or 03_localization).

Usage
-----
    python 04_channel_charting.py [scene_name] [results_dir]

    scene_name   — name of the scene folder (default: "Otaniemi_small")
    results_dir  — optional override for the output directory

Change the scene_name argument in main() to switch between scenes.
"""

